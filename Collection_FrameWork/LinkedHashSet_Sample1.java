package Collection_FrameWork;

import java.util.LinkedHashSet;

public class LinkedHashSet_Sample1
{
	public static void main(String[] args) 
	{
		LinkedHashSet l = new LinkedHashSet();
		
		l.add(100);
		l.add(200);
		l.add(300);
		l.add(400);
		
		System.out.println(l); //[100, 200, 300, 400]
		
		
		
	}
}
